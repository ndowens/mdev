#!/bin/sh
setkeycodes 0x74 148 # User key
setkeycodes 0x75 150 # 0x75 www
setkeycodes 0x78 155 # 0x78 mail
setkeycodes 0x82 227 # Fn+F4 CRT/LCD (high keycode: "displaytoggle")
setkeycodes 0x83 236 # Fn+F2
setkeycodes 0x84 148 # Fn+F5 backlight on/off
setkeycodes 0x86 238 # Fn+F9
setkeycodes 0x88 225 # Fn-Up
setkeycodes 0x89 224 # Fn-Down
setkeycodes 0xB1 149 # Fn+F7 run Samsung Magic Doctor (keypressed event is generated twice)
setkeycodes 0xB3 202 # Fn+F8 switch power mode (battery/dynamic/performance)
setkeycodes 0xB4 238 # Fn+F9 (X60P)
setkeycodes 0xF7 192 # Fn+F10 Touchpad on
setkeycodes 0xF9 193 # Fn+F10 Touchpad off
